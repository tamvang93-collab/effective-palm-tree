function extractProviderTitlesFromBigtool(providerKey) {
  const key = providerKey.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
  const re = new RegExp(`'${key}'\\s*:\\s*\\[([\\s\\S]*?)\\]\\s*(,|\\n\\s*})`);
  const m = bigtoolGameListJsRaw.match(re);
  if (!m) return [];

  const block = m[1];
  const titles = [];
  const titleRe = /'([^']+)'/g;
  let match;
  while ((match = titleRe.exec(block))) {
    titles.push(match[1]);
  }

  return titles;
}

