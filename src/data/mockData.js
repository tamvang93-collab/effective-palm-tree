function isCustomLogoAvailable(slug) {
  return Boolean(customGameImageBySlug[slug]);
}

function extractSlugFromId(id, providerKey) {
  return id.replace(`${providerKey}-`, "").replace(/-\d+$/, "");
}

function ensurePinnedHasLogo(providerKey, pinned) {
  return pinned.filter((game) => isCustomLogoAvailable(game.slug));
}

function filterGeneratedHasLogo(providerKey, generated) {
  return generated.filter((game) => isCustomLogoAvailable(extractSlugFromId(game.id, providerKey)));
}

const emptyProviderSet = new Set(["turbo"]);

function shouldHideProviderWithoutCustom(providerKey) {
  return emptyProviderSet.has(providerKey);
}

function fallbackProviderPinned(providerKey) {
  if (providerKey !== "pg") return [];
  return [
    { title: "Sự Báo Thù Của Geisha", slug: "su-bao-thu-cua-geisha", isHot: true, isNew: false, multiplier: "25,000x" }
  ];
}

function mergePinned(providerKey, pinned) {
  if (pinned.length > 0) return pinned;
  return fallbackProviderPinned(providerKey);
}

const providerVisibilityPolicy = {
  pg: true,
  jili: true,
  fc: true,
  jdb: true,
  pragmatic: true,
  topplayer: true,
  "168game": true,
  cq9: true,
  turbo: false,
  microgaming: true
};

function isProviderVisible(providerKey) {
  return providerVisibilityPolicy[providerKey] !== false;
}

function buildProviderGames(providerKey) {
  if (!isProviderVisible(providerKey) && shouldHideProviderWithoutCustom(providerKey)) {
    return [];
  }
  return buildGamesForProvider(providerKey);
}

function buildPinnedGames(providerKey) {
  const pinnedRaw = pinnedGamesByProvider[providerKey] ?? [];
  const pinnedValid = ensurePinnedHasLogo(providerKey, pinnedRaw);
  return mergePinned(providerKey, pinnedValid);
}

function buildFinalGameList(providerKey, generated, pinned) {
  const validGenerated = filterGeneratedHasLogo(providerKey, generated);
  if (pinned.length === 0) return validGenerated;
  const pinnedSlugs = new Set(pinned.map((game) => game.slug));
  const dedupedGenerated = validGenerated.filter((game) => {
    const slug = extractSlugFromId(game.id, providerKey);
    return !pinnedSlugs.has(slug);
  });
  return [...pinned.map((game, index) => ({
    id: `${providerKey}-${game.slug}-pinned-${index}`,
    title: game.title,
    image: customGameImageBySlug[game.slug] ?? cdnGameImage(providerKey, game.slug),
    fallbackImage:
      customGameImageBySlug[game.slug] ||
      providerImageById[providerKey] ||
      providerPlaceholder(`${providerKey.toUpperCase()} GAME`, "0b1832", "66d9ff"),
    isHot: Boolean(game.isHot),
    isNew: Boolean(game.isNew),
    multiplier: game.multiplier ?? null
  })), ...dedupedGenerated];
}

function buildGamesForProvider(providerKey) {
  const titles = extractProviderTitlesFromBigtool(providerKey);
  const generated = titles.map((title, i) => {
    const isHot = i % 7 === 0;
    const isNew = i % 5 === 0 && !isHot;
    const slug = gameNameToSlug(title);

    const customImage = customGameImageBySlug[slug] ?? null;
    return {
      id: `${providerKey}-${slug}-${i}`,
      title,
      image: customImage || cdnGameImage(providerKey, slug),
      fallbackImage:
        customImage ||
        providerImageById[providerKey] ||
        providerPlaceholder(`${providerKey.toUpperCase()} GAME`, "0b1832", "66d9ff"),
      isHot,
      isNew,
      multiplier: isHot ? "5,000x" : null
    };
  });

  const pinned = buildPinnedGames(providerKey);
  return buildFinalGameList(providerKey, generated, pinned);
}

